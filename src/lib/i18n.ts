export const getTranslation = (key: string, language: Language, props?: AppRouterProps): string => {
  let text = translations[language][key as keyof typeof translations['vi']] || key;
  if(props) {
    const dictionary = {
      tencongty: props.data_info.tencongty[language],
      diachi: props.data_info.diachi[language],
      email: props.data_info.email,
      sodienthoai: props.data_info.sodienthoai,
      domain: props.data_info.domain
    };
    text = replacePlaceholders(text, dictionary);
  }

  return text;
};

export const getContent = (pages: any, key: string, language: string) => {
  let link = pages.filter((a)=>{
    if(!a.slug) return false;
    return a.key === key && a.lang === language;
  })[0]?.slug;
  if(link)
    link = "/" + link;
  return link;
}
